#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main()
{
	char	src[] = "truffle oil";
	char	dest[20];
	int	n;

	n = 7;

	ft_strncpy(dest,src,n);

	write(1, dest, n);
}
