#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	int	b;

	b = ft_strlen("verdade");
	printf("%d", b);
	return (0);
}
