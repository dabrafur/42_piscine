#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}

int	main(void)
{
	char	val1[] = "socorro!!!";
	char	val2[] = "Socorro";

	printf("%d\n", ft_strcmp(val1, val2));
	return (0);
}
